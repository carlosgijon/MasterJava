public class BucleDoWhile {
    public static void main(String[] args) {
        int i = 20;
        System.out.println("Voy a entrar al bucle");

        do {
            i++;
            System.out.println("Valor de i = " + i);

        } while(i <= 10);

        System.out.println("He salido del bucle");
    }
}
