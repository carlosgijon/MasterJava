public class PruebaVariables {
    public static void main(String[] args) {
        float float1;
        double double1;

        float1 = 21.1231157111F;  // Se le pone la F al final para indicar que es float ya que en java los numeros con decimales son double
        double1 = 19.11437687311E+9D;  // Con la D final se indica que es double

        System.out.println("El valor de float1 es " + float1);
        System.out.println("El valor de double1 es " + double1);
    }
}
